# An simple example of a while loop, as long as a condition is true, keep looping through
# I.E. as long as i is less than or equal to 10, keep printing i and adding 1.
i = 1

while i <= 10:
    print(i)
    # shorthand version of i = i + 1
    i += 1

print("Done with loop")