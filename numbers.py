# An int
print(2)
# A float
print(2.0987)
# Negative numbers
print(-2.0987)
# Addition
print(3 + 4.5)
# Subtraction
print(3 - 4.5)
# Division
print(3/4.5)
# Multiplication
print(3 * 4.5)
# Order of operation by parentheses
print(3 * (4 + 5))
# Modules
print(10 % 3)

# Variables can be numbers
my_num = 5
print(my_num)
# Convert number into a string
print(str(my_num))
# Concade a number variable to a string
print(str(my_num) + " is my favorite number.")
# Absolute value
my_num = -5
print(abs(my_num))
# Power - 3 to the power of 2
print(pow(3,2))
# Max - which value is higher
print(max(4,6))
# Min - which value is lower
print(min(4,6))
# Round a number
print(round(3.2))
print(round(3.7))

# import all math functions inside the math module
from math import *

# Floor - removes any decimal value
print(floor(3.7))
# Ceil - always rounds to the next number when given a float
print(ceil(3.2))
# square root - note gives the float
print(sqrt(36))


