# A list of friends
friends = ["Kevin", "Karen", "Jim", "Oscar", "Toby"]
# lists can be strings, numbers, booleans

# Prints the entire friends list
print(friends)
# Print the index parts of the friend list
print(friends[0])
print(friends[2])
# Print the index parts from the back of the list
print(friends[-2])
# Print certain parts of the list
print(friends[1:3])
# Modify a list
friends[1] = "Mike"
# Print out the change
print(friends[1])
